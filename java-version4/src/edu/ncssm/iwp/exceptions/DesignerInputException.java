package edu.ncssm.iwp.exceptions;

public class DesignerInputException extends Exception {
	private static final long serialVersionUID = 1L;
	public DesignerInputException() {
		super();
		
	}

	public DesignerInputException(String message) {
		super(message);
		
	}

	public DesignerInputException(Throwable cause) {
		super(cause);
		
	}

	public DesignerInputException(String message, Throwable cause) {
		super(message, cause);
		
	}

}
